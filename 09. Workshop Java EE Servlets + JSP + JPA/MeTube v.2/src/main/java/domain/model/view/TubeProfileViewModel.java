package domain.model.view;

public class TubeProfileViewModel {
    private String id;
    private String title;
    private String author;

    public TubeProfileViewModel() {
    }

    public TubeProfileViewModel(String title, String author) {
        this.title = title;
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
